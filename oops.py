# creating add,sub,mul,div,mod class:
class Myclass:
    def add(a,b):
        return a+b
    def sub(a,b):
        return a-b
    def mul(a,b):
        return a*b
    def mod(a,b):
        return a%b
    def div(a,b):
        return a/b
obj1=Myclass
obj2=Myclass
print(obj1.add(2,5))
print(obj2.sub(12,5))




